
public class OperatorMain10 {

	public static void main(String[] args) {
		System.out.println("===조건(삼항)연산자===");
		
		int a = 5, b = 10;
		int max; //최대값 저장
		int min; //최소값 저장
			// 조건 ? 참값 : 거짓값
		max = a > b ? a : b;
		System.out.println("최대값 : " + max);

		min = a < b ? a : b;
		System.out.println("최소값 : " + min);
		
		
		
		
		
	}

}
