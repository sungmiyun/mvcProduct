
public class ForMain01 {

	public static void main(String[] args) {
		
		  // 초기식 ; 조건식 ; 증감식
		for(int i = 1; i <= 5; i++) { //for문 시작
			System.out.println(i);
		} //for문 종료
		
		System.out.println("==============");
		
		//수행문이 한 줄인 경우 for문 {}블럭 생략 가능
		for(int i = 1; i <= 5; i++)
			System.out.println(i);
		
		System.out.println("프로그램 끝");
		
		
		
		
	}

}
