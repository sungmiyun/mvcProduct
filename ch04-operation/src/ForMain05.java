
public class ForMain05 {

	public static void main(String[] args) {
		//2단부터 9단까지 출력
			//단
		for(int dan = 2; dan <= 9; dan++) {
			System.out.println("** " + dan + "단 **");
			
			//곱해지는 숫자
			for(int i = 1; i <= 9; i++) {
				System.out.println(dan + " * " + i + " = " + dan * i);
			}
			
			
			
			
			
			
		}
		
		
		
		
		
		
		
		
		
	}

}
